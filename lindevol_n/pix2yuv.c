long get_int32(unsigned char *i32)
{
  if (i32[0] & 0x80)
  {
    i32[0] ^= 0xff;
    i32[1] ^= 0xff;
    i32[2] ^= 0xff;
    i32[3] ^= 0xff;
    return (-i32[0] * 16777216 - i32[1] * 65536 - i32[2] * 256 - i32[3] - 1);
  }
  else
    return (+i32[0] * 16777216 + i32[1] * 65536 + i32[2] * 256 + i32[3]);
}


void rgb2ycbcr(double r, double g, double b, unsigned char *y, unsigned char *cb, unsigned char *cr)
{
  double y1, cb1, cr1;

  y1 = 0.299 * r + 0.587 * g + 0.114 * b;
  cb1 = -0.1687 * r - 0.3313 * g + 0.5 * b;
  cr1 = 0.5 * r - 0.4187 * g - 0.0813 * b;
/*
  *y = y1;
  *cb = cb1 + 128;
  *cr = cr1 + 128;
*/
  *y = y1 * 219.0 / 255.0 + 16;
  *cb = cb1 * 224.0 / 255.0 + 128;
  *cr = cr1 * 224.0 / 255.0 + 128;
}


void ycbcr_graphstring(const char *s, long x0, long y0, int pixsize, const FIXFONT_SPEC *fspec,
	unsigned char *y_buffer, unsigned char *cb_buffer, unsigned char *cr_buffer)
{
  unsigned char c, y_char, cb_char, cr_char, yb_char, cbb_char, crb_char;
  size_t i = 0;
  int xf, yf, x, y;
  int y_offset = ((fspec->width - 1) / 8) + 1;
  int c_offset = fspec->height * y_offset;

  rgb2ycbcr(fspec->r, fspec->g, fspec->b, &y_char, &cb_char, &cr_char);
  rgb2ycbcr(fspec->rb, fspec->gb, fspec->bb, &yb_char, &cbb_char, &crb_char);
  while ((c = s[i++]))
  {
    for (yf = 0; yf < fspec->height; yf++)
    {
      for (xf = 0; xf < fspec->width; xf++)
      {
	if ((fspec->fnt[c * c_offset + yf * y_offset + (xf / 8)] & (128U >> (xf % 8))))
	{
	  for (y = y0 + yf * pixsize; y < y0 + yf * pixsize + pixsize; y++)
	  {
	    for (x = x0 + xf * pixsize; x < x0 + xf * pixsize + pixsize; x++)
	    {
	      y_buffer[y * 352 + x] = y_char;
	      cb_buffer[y * 352 + x] = cb_char;
	      cr_buffer[y * 352 + x] = cr_char;
	    }
	  }
	}
	else
	{
	  for (y = y0 + yf * pixsize; y < y0 + yf * pixsize + pixsize; y++)
	  {
	    for (x = x0 + xf * pixsize; x < x0 + xf * pixsize + pixsize; x++)
	    {
	      y_buffer[y * 352 + x] = yb_char;
	      cb_buffer[y * 352 + x] = cbb_char;
	      cr_buffer[y * 352 + x] = crb_char;
	    }
	  }
	}
      }
    }
    x0 += fspec->width * pixsize;
  }
}


