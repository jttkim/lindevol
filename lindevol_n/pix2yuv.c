int read_timespec(LINEFILE *lf, long *startg, long *endg, size_t length, char *legend)
{
  char buf[1024] = "", err[1280], *s, *s1;

  while (!buf[0])
  {
    if (l_fgets0(buf, 1024, lf) == NULL)
      return (-1);
  }
  *startg = strtol(buf, &s, 10);
  if (s == buf)
  {
    sprintf(err, "read_timespec: erroneous entry \"%s\"", buf);
    l_errmsg(lf, err);
    return (-1);
  }
  while (*s && isspace(*s))
    s++;
  if (!strncmp(s, "end", 3))
  {
    *endg = LONG_MAX;
    s1 = s + 3;
  }
  else
  {
    if (*s == '\0')
    {
      sprintf(err, "read_timespec: erroneous entry \"%s\"", buf);
      l_errmsg(lf, err);
      return (-1);
    }
    *endg = strtol(s, &s1, 10);
    if (s1 == s)
    {
      sprintf(err, "read_timespec: erroneous entry \"%s\"", buf);
      l_errmsg(lf, err);
      return (-1);
    }
  }
  while (*s1 && isspace(*s1))
    s1++;
  strncpy(legend, s1, length);
  return (0);
}


